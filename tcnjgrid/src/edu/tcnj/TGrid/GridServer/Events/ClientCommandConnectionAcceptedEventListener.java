package edu.tcnj.TGrid.GridServer.Events;

/**
 * The ClientCommandConnectionAcceptedEventListener interface should be implemented 
 * by any class interested in receiving notification of 
 * ClientCommandConnectionAcceptedEvents
 * 
 * @author Stephen
 * @see GridServer.Events.ClientCommandConnectionAcceptedEvent
 */
public interface ClientCommandConnectionAcceptedEventListener extends java.util.EventListener
{
	/**
	 * Triggered when a new client command connection is accepted
	 */
	public void clientCommandConnectionAccepted(ClientCommandConnectionAcceptedEvent e);
}
