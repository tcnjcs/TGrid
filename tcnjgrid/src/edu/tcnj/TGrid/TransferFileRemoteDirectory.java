package edu.tcnj.TGrid;

/**
 * Enumerates the possible directories that a transferred file can be placed in
 * 
 * @author Stephen
 */
public enum TransferFileRemoteDirectory
{    
	/**
	 * Current directory
	 */
	CURRENT_DIR,
	
	/**
	 * Data directory
	 */
	DATA_DIR,
	
	/**
	 * Java class files directory
	 */
	JAVA_CLASS_DIR,
	
	/**
	 * Executables directory
	 */
	EXECUTABLES_DIR,
	
	/**
	 * General directory
	 */
	MISC_DIR
}
